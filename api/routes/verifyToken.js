//Importiamo jwt
const jwt = require("jsonwebtoken");

//Scriviamo la nostra funzione di verifica token. Usiamo postman per fare i test scegliendo header al posto di post e poi token e scrivendo il nostro token
const verifyToken = (req, res, next) => {
    const authHeader = req.headers.token;
    if (authHeader) {
        const token = authHeader.split(" ")[1];
        jwt.verify(token, process.env.JWT_SEC, (err, user) => {
            if (err) res.status(403).json("Token is not valid!");
            req.user = user;
            next();
        });
    } else {
        return res.status(401).json("You are not authenticated!");
    }
};
//Verifichiamo se l'utente ha il token che si vuole usare 
const verifyTokenAndAuthorization = (req, res, next) => {
    verifyToken(req, res, () => {
        if (req.user.id === req.params.id || req.user.isAdmin) {
            next();
        } else {
            res.status(403).json("You are not alowed to do that!");
        }
    });
};

// Verifica del token nel caso l'utente sia admin
const verifyTokenAndAdmin = (req, res, next) => {
    verifyToken(req, res, () => {
        if (req.user.isAdmin) {
            next();
        } else {
            res.status(403).json("You are not alowed to do that!");
        }
    });
};
module.exports = { verifyToken, verifyTokenAndAuthorization, verifyTokenAndAdmin }